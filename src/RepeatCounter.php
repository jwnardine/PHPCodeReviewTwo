<?php

class CountRepeat
  {
      function oneWord($input)
      {
        return $input;
      }
      function multipleWords($input)
      {
          return $input;
      }
      function findWord($input_sentence, $input_word)
        {
            $explode_words = explode(" ", $input_sentence);
            foreach($explode_words as $word) {
                if ($word = $input_word);
            }
                return true;
        }
  }

